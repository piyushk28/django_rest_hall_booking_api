from django.conf.urls import url
from django.conf.urls.static import static
from django.contrib import admin
from django.urls import path, include
from drf_yasg.views import get_schema_view
from drf_yasg import openapi
from rest_framework import permissions
from django.conf import settings
from rest_framework_jwt.views import obtain_jwt_token, refresh_jwt_token, verify_jwt_token

schema_view = get_schema_view(
    openapi.Info(
        title="Booking Management",
        default_version='v1',
        description="Hall Booking Management",
        terms_of_service="https://www.google.com/policies/terms/",
        contact=openapi.Contact(email="piyushkhandelwal98@hotmail.com"),
        license=openapi.License(name="BSD License"),
    ),
    public=True,
    permission_classes=(permissions.AllowAny,),
)

urlpatterns = [
    path('admin/', admin.site.urls),
    path('booking/', include('booking.urls')),

    url(r'^accounts/login/', obtain_jwt_token),
    url(r'^accounts/refresh-token/', refresh_jwt_token),
    url(r'^accounts/verify-token/', verify_jwt_token),

    # swagger and redoc support
    url(r'^swagger(?P<format>\.json|\.yaml)$', schema_view.without_ui(cache_timeout=0), name='schema-json'),
    url(r'^swagger/$', schema_view.with_ui('swagger', cache_timeout=0), name='schema-swagger-ui'),
    url(r'^redoc/$', schema_view.with_ui('redoc', cache_timeout=0), name='schema-redoc'),
    url(r'^api.json/$', schema_view.without_ui(cache_timeout=0), name='schema-swagger-ui'),

]

urlpatterns += static(settings.STATIC_URL, document_root=settings.STATIC_ROOT)
urlpatterns += static(settings.MEDIA_URL, document_root=settings.MEDIA_ROOT)
